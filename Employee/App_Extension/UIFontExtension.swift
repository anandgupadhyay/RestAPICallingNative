import UIKit

extension UIFont {
    func VerdanaRegular(size:CGFloat) -> UIFont {
        return UIFont(name: "Verdana", size: size)!
    }
    func VerdanaBold(size:CGFloat) -> UIFont {
        return UIFont(name: "Verdana-Bold", size: size)!
    }
}
